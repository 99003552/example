package com.honda.ccsportal.model;

public class CustomerSearchEngine {
	
	private int engine_id;
    public String boatName;
	private String engine_status;
	private String customer_id;
	private String serialNumber;
	private String hullnumber;
	
	
	public int getEngine_id() {
		return engine_id;
	}
	public void setEngine_id(int engine_id) {
		this.engine_id = engine_id;
	}
	public String getBoatName() {
		return boatName;
	}
	public void setBoatName(String boatName) {
		this.boatName = boatName;
	}
	public String getEngine_status() {
		return engine_status;
	}
	public void setEngine_status(String engine_status) {
		this.engine_status = engine_status;
	}
	public String getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(String customer_id) {
		this.customer_id = customer_id;
	}
	public String getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}
	public String getHullnumber() {
		return hullnumber;
	}
	public void setHullnumber(String hullnumber) {
		this.hullnumber = hullnumber;
	}
	
	

}
